class Weather {
  double id;
  String main;
  String description;
  String icon;

  Weather({
    required this.id,
    required this.main,
    required this.description,
    required this.icon,
  });

  factory Weather.fromJson(Map<String, dynamic> json) {
    return Weather(
      id: json['id'].toDouble(),
      main: json['main'],
      description: json['description'],
      icon: json['icon'],
    );
  }

  // Method to convert Weather instance to JSON
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'main': main,
      'description': description,
      'icon': icon,
    };
  }
}
